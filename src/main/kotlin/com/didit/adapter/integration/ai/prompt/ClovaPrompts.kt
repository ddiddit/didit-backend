package com.didit.adapter.integration.ai.prompt

import com.didit.domain.auth.Job

object ClovaPrompts {
    fun buildDeepQuestionPrompt(
        job: Job,
        answers: List<String>,
    ): String {
        val formattedAnswers =
            answers
                .mapIndexed { index, answer ->
                    "답변 ${index + 1}: $answer"
                }.joinToString("\n")

        val jobSpecificContext =
            when (job) {
                Job.DEVELOPER ->
                    """
                    개발자 관점 핵심:
                    - 기술적 성장, 코드 품질, 테스트, 리팩토링, 기술 부채
                    - 협업 방식, 코드 리뷰, 배포 경험, 장애 대응, 생산성 개선
                    - 단순 감상이 아니라 문제 해결 과정과 재현 가능한 개선 방향
                    """.trimIndent()

                Job.PLANNER ->
                    """
                    기획자 관점 핵심:
                    - 제품 목표, 사용자 문제 정의, 우선순위 설정, 요구사항 조율
                    - 데이터 기반 판단, 성과 측정, 이해관계자 협업, 실행 가능성
                    - 단순 회고가 아니라 전략적 판단과 다음 실험 방향
                    """.trimIndent()

                Job.DESIGNER ->
                    """
                    디자이너 관점 핵심:
                    - 사용자 경험, 디자인 의사결정, 디자인 시스템, 프로토타이핑
                    - 사용자 피드백 반영, 협업 과정, 일관성, 문제 해결 방식
                    - 단순 결과보다 판단 근거와 개선 방향
                    """.trimIndent()
            }

        return """
            당신은 사용자의 회고를 돕는 코치입니다.
            아래는 사용자의 회고 1~3번 답변입니다.

            $formattedAnswers

            $jobSpecificContext

            목표:
            위 답변을 바탕으로 사용자가 더 깊이 성찰할 수 있도록, 직군에 맞는 맞춤형 추가 질문(Deep Question) 1개를 생성하세요.

            반드시 지킬 조건:
            1. 답변 1~3에서 드러난 내용만 바탕으로 질문하세요.
            2. 답변에 없는 사실을 추측하거나 새로 만들어내지 마세요.
            3. 사용자의 구체적인 표현, 상황, 문제의식을 자연스럽게 반영하세요.
            4. 너무 일반적인 질문은 금지합니다.
               예: "무엇을 배웠나요?", "어떻게 느꼈나요?" 같은 질문 금지
            5. 직군 특성이 드러나야 합니다.
            6. 사용자가 다음 행동이나 판단 기준까지 떠올릴 수 있는 질문이어야 합니다.
            7. 질문은 한국어 존댓말 한 문장으로 작성하세요.
            8. 길이는 45~90자 내외로 유지하세요.
            9. 출력은 질문 한 문장만 하세요. 설명, 제목, 따옴표, 번호는 쓰지 마세요.

            좋은 질문의 예시 특성:
            - 사용자의 실제 답변 맥락을 반영함
            - 구체적인 원인, 판단, 우선순위, 개선 행동을 떠올리게 함
            - 직군별 전문성이 느껴짐
            """.trimIndent()
    }

    fun buildSummaryPrompt(
        job: Job,
        allAnswers: List<String>,
    ): String {
        val formattedAnswers =
            allAnswers
                .mapIndexed { index, answer ->
                    "답변 ${index + 1}: $answer"
                }.joinToString("\n")

        val jobSpecificContext =
            when (job) {
                Job.DEVELOPER ->
                    """
                    개발자 관점 핵심:
                    - 기술적 성장, 코드 품질, 테스트, 리팩토링, 기술 부채, 협업 방식
                    - 구현 과정의 문제 해결, 개발 생산성, 유지보수성, 재발 방지 관점 강조
                    """.trimIndent()

                Job.PLANNER ->
                    """
                    기획자 관점 핵심:
                    - 문제 정의, 요구사항 구체화, 우선순위, 사용자 가치, 성과 측정
                    - 협업 조율, 전략적 판단, 실행 가능성과 검증 가능성 강조
                    """.trimIndent()

                Job.DESIGNER ->
                    """
                    디자이너 관점 핵심:
                    - 사용자 경험, 시각적/구조적 일관성, 디자인 의사결정 근거
                    - 피드백 반영, 협업, 사용성 개선, 디자인 프로세스의 성숙도 강조
                    """.trimIndent()
            }

        return """
            당신은 사용자의 회고 답변을 정리해주는 전문 코치입니다.
            아래는 사용자의 전체 회고 답변입니다.

            $formattedAnswers

            $jobSpecificContext

            목표:
            위 답변만을 근거로 전체 회고를 간결하지만 밀도 있게 요약하세요.

            반드시 지킬 조건:
            1. 사용자가 실제로 말한 내용만 바탕으로 정리하세요.
            2. 답변에 없는 성과, 문제, 계획, 감정, 수치를 지어내지 마세요.
            3. 모호한 표현은 과장하지 말고, 답변 수준에 맞게 정리하세요.
            4. 직군 특성에 맞는 관점이 드러나야 합니다.
            5. 문장은 간결하고 실무적으로 작성하세요.
            6. 각 bullet은 한 줄 또는 두 줄 이내로 짧게 작성하세요.
            7. 전체 형식은 반드시 아래 마크다운 구조를 정확히 따르세요.
            8. 출력은 한국어로만 작성하세요.

            출력 형식:
            ## 주요 성과
            - ...
            - ...

            ## 개선 필요 사항
            - ...
            - ...

            ## 액션 아이템
            - ...
            - ...

            ## 직군별 인사이트
            - ...

            추가 규칙:
            - "주요 성과"는 최대 2개
            - "개선 필요 사항"은 최대 2개
            - "액션 아이템"은 최대 2개
            - "직군별 인사이트"는 1개만 작성
            - 액션 아이템은 실제로 실행 가능한 행동 중심으로 작성
            - 직군별 인사이트는 일반론보다, 답변에서 드러난 패턴을 바탕으로 작성
            - 전체 분량은 250~450자 내외로 유지

            나쁜 예:
            - 답변에 없는 성과를 추가함
            - 지나치게 추상적인 조언만 작성함
            - 형식을 깨고 긴 문단으로 씀

            좋은 예:
            - 답변의 핵심을 압축해서 정리함
            - 문제와 다음 행동이 연결됨
            - 직군 관점이 자연스럽게 드러남
            """.trimIndent()
    }
}
